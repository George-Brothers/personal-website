/* ============================================================
   George Brothers — js/demos/ai-for-beginners.js
   TRY IT demo for /work/ai-for-beginners. Mounts into
   [data-demo="ai-for-beginners"], replacing its [data-demo-fallback]
   with a jargon decoder: a short realistic sentence with seven
   loaded AI terms highlighted as buttons. Activating one (click or
   keyboard) reveals the plain-English explanation the guidebook
   would give — concrete, no cleverness, no condescension. All
   content hardcoded: no network calls, no dependencies.
   Plain ES2017, no modules. Loaded with <script defer>.
   ============================================================ */
(function () {
  'use strict';

  var host = document.querySelector('[data-demo="ai-for-beginners"]');
  if (!host) return;

  var fallback = host.querySelector('[data-demo-fallback]');
  if (!fallback) return;

  // Definitions, written the way the guidebook explains a term: what it
  // actually is, in concrete terms, with no hand-waving and no "just trust
  // me." Wrong here would be worse than no demo at all, so keep these plain
  // and correct rather than clever.
  var TERMS = {
    'fine-tuning': {
      label: 'Fine-tuning',
      def: 'Taking an already-trained model and training it further on a narrower set of examples so it gets better at one specific job — a support tone, a coding style, your own product names. It changes the model itself, which costs more and takes longer than just giving it instructions in the moment.'
    },
    'parameters': {
      label: 'Parameters',
      def: 'The internal numbers a model learns during training — its adjustable dials, essentially. A "70-billion-parameter model" has 70 billion of them. More parameters usually means more capacity to capture patterns, and more compute needed to run it.'
    },
    'context-window': {
      label: 'Context window',
      def: 'How much text, measured in tokens, a model can hold in view at once for a single conversation or request. Push past it and the oldest material gets dropped, the same way you would lose track of the start of a long meeting. It resets between separate conversations — it is a workspace, not a memory.'
    },
    'hallucination': {
      label: 'Hallucination',
      def: 'When a model states something false in the same confident voice it uses for something true. It is not lying on purpose — it is predicting text that sounds plausible, and plausible is not the same thing as true. It shows up most when the model does not actually know the answer but produces one anyway.'
    },
    'rag': {
      label: 'RAG (retrieval-augmented generation)',
      def: 'Instead of trusting only what the model memorized during training, RAG looks up real documents first and hands the model the relevant passages to read before it answers. It is how a model gets facts right about things that happened after its training ended, or documents it was never shown at all.'
    },
    'embedding': {
      label: 'Embedding',
      def: 'A list of numbers that stands in for the meaning of a piece of text, arranged so that similar meanings land near each other in that number space. It is how a computer, which only handles numbers, gets to compare meaning at all — two sentences with different words but the same idea end up close together.'
    },
    'token': {
      label: 'Token',
      def: 'The actual chunk of text a model reads and writes — often a piece of a word, not a whole one. "Strawberry" might cost two or three tokens. Everything sent to a model or generated by it is counted, priced, and limited in tokens, not words or characters.'
    }
  };

  // The sentence the demo reads out, split into plain-text runs and
  // highlighted terms in reading order. Every term in TERMS is used exactly
  // once, in a sentence that sounds like something you'd actually overhear.
  var SEGMENTS = [
    { text: 'Marketing wants us to ' },
    { term: 'fine-tuning', text: 'fine-tune' },
    { text: ' a 70-billion-' },
    { term: 'parameters', text: 'parameter' },
    { text: ' model, but half our support tickets blow past the ' },
    { term: 'context-window', text: 'context window' },
    { text: ' before it ever reaches the actual question. Legal is worried about ' },
    { term: 'hallucination', text: 'hallucination' },
    { text: ' in the summaries. Engineering thinks ' },
    { term: 'rag', text: 'RAG' },
    { text: ' would fix the accuracy problem, since it pulls in real documents by comparing ' },
    { term: 'embedding', text: 'embeddings' },
    { text: ' instead of trusting the model’s memory. Someone will explain what a ' },
    { term: 'token', text: 'token' },
    { text: ' even is by Friday, hopefully.' }
  ];

  function buildDemo() {
    var wrap = document.createElement('div');
    wrap.className = 'jd';

    var panelId = 'jd-panel';

    var para = document.createElement('p');
    para.className = 'jd-para';

    var buttons = [];

    SEGMENTS.forEach(function (seg) {
      if (seg.term) {
        var btn = document.createElement('button');
        btn.type = 'button';
        btn.className = 'jd-term';
        btn.textContent = seg.text;
        btn.setAttribute('aria-expanded', 'false');
        btn.setAttribute('aria-controls', panelId);
        btn.dataset.term = seg.term;
        btn.addEventListener('click', function () { activate(seg.term, btn); });
        buttons.push(btn);
        para.appendChild(btn);
      } else {
        para.appendChild(document.createTextNode(seg.text));
      }
    });

    var panel = document.createElement('div');
    panel.className = 'jd-panel';
    panel.id = panelId;
    panel.setAttribute('aria-live', 'polite');

    var placeholder = document.createElement('p');
    placeholder.className = 'jd-panel-empty';
    placeholder.textContent = 'Click or tab to any highlighted term to see it explained in plain English.';
    panel.appendChild(placeholder);

    var activeTerm = null;

    function activate(term, btn) {
      var reopening = activeTerm === term;
      buttons.forEach(function (b) {
        b.setAttribute('aria-expanded', 'false');
        b.classList.remove('is-active');
      });
      panel.textContent = '';

      if (reopening) {
        // Toggling the same term closes it back to the placeholder.
        activeTerm = null;
        panel.appendChild(placeholder);
        return;
      }

      activeTerm = term;
      btn.setAttribute('aria-expanded', 'true');
      btn.classList.add('is-active');

      var data = TERMS[term];
      var label = document.createElement('p');
      label.className = 'jd-panel-term';
      label.textContent = data.label;
      var def = document.createElement('p');
      def.className = 'jd-panel-def';
      def.textContent = data.def;
      panel.appendChild(label);
      panel.appendChild(def);
    }

    wrap.appendChild(para);
    wrap.appendChild(panel);
    return wrap;
  }

  fallback.replaceWith(buildDemo());
})();
